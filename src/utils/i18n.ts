type DeepI18n<T> =
  T extends Record<string, unknown> ?
    {
      [P in keyof T & string]: T[P][keyof T[P]] extends object ? `${P}.${DeepI18n<T[P]>}` : P
    }[keyof T & string] : never


export type I18nPath = DeepI18n<typeof i18n>
